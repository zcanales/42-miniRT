#include "../../includes/miniRT.h"

void	check_max_color(t_color *c)
{
	if (c->r > 1)
		c->r = 1;
	if (c->r < 0)
		c->r = 0;
	if (c->g > 1)
		c->g = 1;
	if (c->g < 0)
		c->g = 0;
	if (c->b > 1)
		c->b = 1;
}
